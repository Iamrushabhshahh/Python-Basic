# IT ALLOWS YOU TO MANUPILATE SINGLE BIT OF DATA
#   List of Bitwise Operator
#     1 ) & - Conjuction
#     2 ) | - Disjunction 
#     3 ) ~ - Negation
#     4 ) ^ - Exclusie



#     1 ) & - Conjuction

print(15 & 22)

#     2 ) | - Disjunction 

print(15 | 22)

#     4 ) ^ - Exclusie

print(15 ^ 22) 

#     3 ) ~ - Negation

print(~22)


#  Bit Shifting 
# >> Shifting right
#  << Shifting Left

print( 22 >> 1 )
print( 22 << 1 )